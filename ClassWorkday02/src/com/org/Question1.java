package com.org;
//1. Program to demonstrate Arithmetic Operators
public class Question1 {
   public static void main (String[] args) {
	   
	   int a=10;
	   int b=20;
	   
	   int sum  = a+b;
	   int sub = a-b;
	   int multiplication = a*b;
	   int division = a/b;
	   
	   System.out.println("The Sum of Two Number is : "+sum);
	   System.out.println("The sub of Two Number is : "+sub);
	   System.out.println("The multiplication of Two Number is : "+multiplication);
	   System.out.println("The division of Two Number is : "+division);
	   
   }
}
